<template>
  <div class="main-sidebar">
    <el-row class="sidebar-tool">
      <div class="sidebar-menu">
        <el-menu
          :default-active="String(state.activeIndex)"
          active-text-color="#ffd04b"
          align="center"
        >
          <el-menu-item @click="$router.push('/')">
            <span>홈</span>
          </el-menu-item>
          <el-menu-item
            v-for="i in 10"
            :key="i"
            :index="i"
            @click="conferenceSelect(i)"
            @mousedown.right="mr()"
            @mousedown.stop
            @contextmenu.prevent
          >
            <span>{{ i }}</span>
          </el-menu-item>
          <el-button type="warning"
            ><div
              class="iconify"
              id="main-sidebar-make-room"
              data-inline="false"
              data-icon="entypo:squared-plus"
              style="font-size: 20px;"
            ></div
          ></el-button>
          <el-button class="button" @click="signOut">로그아웃</el-button>
          <div>
            <el>{{ username }}</el>
          </div>
          <img :src="`${img}`" style="width : 30px; border-radius: 70%" />
        </el-menu>
      </div>
    </el-row>
  </div>
  <!-- 방 정보 수정 확인 dialog -->
  <el-dialog title="방 정보를 수정하시겠습니까?" v-model="a" width="30%">
    <template #footer>
      <span class="dialog-footer">
        <el-button @click="a = false">아니오</el-button>
        <el-button type="primary" @click="mvModify()">네</el-button>
      </span>
    </template>
  </el-dialog>
  <!-- 방 정보 수정 확인 dialog 끝-->
  <!-- 방 정보 수정 dialog 시작 -->
  <el-dialog
    title="방 정보 수정"
    v-model="dialogFormVisible_modify"
    center
    top="10vh"
  >
    <el-divider></el-divider>
    <el-form :model="modifyform">
      <el-form-item
        label="방 제목🏠"
        :label-width="formLabelWidth"
        id="room-make-form-label"
      >
        <el-input
          v-model="roomInfo.name"
          autocomplete="off"
          placeholder=""
        ></el-input>
      </el-form-item>
