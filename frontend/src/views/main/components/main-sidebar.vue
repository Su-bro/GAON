        label="방 코드🔑"
        :label-width="formLabelWidth"
        id="room-make-form-label"
      >
        {{ roomInfo.code }}
      </el-form-item>
    </el-form>
    <template #footer>
      <span class="dialog-footer">
        <el-button class="gaon-button" type="warning" @click="modifyRoom()"
          >적용하기</el-button
        >
        <el-button @click="dialogFormVisible_modify = false" type="info"
          >취소</el-button
        >
      </span>
